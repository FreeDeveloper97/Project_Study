.MODEL	SMALL
.STACK	100H
.DATA
	HOUR	DW	?
	MINUTE	DW	?
	SECOND	DW	?
.CODE
MAIN	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	CALL	INDECM
	XOR	DX,DX
	MOV	BX,60	;나눌 60

	DIV	BX
	MOV	SECOND,DX
	XOR	DX,DX

	DIV	BX
	MOV	MINUTE,DX
	MOV	HOUR,AX

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	AX,HOUR
	CALL	OUTDECM
	MOV	AH,2
	MOV	DL,':'
	INT	21H

	MOV	AX,MINUTE
	CALL	OUTDECM
	MOV	AH,2
	MOV	DL,':'
	INT	21H

	MOV	AX,SECOND
	CALL	OUTDECM
	
	MOV	AH,4CH
	INT	21H
MAIN	ENDP

INCLUDE	INDECM.ASM
INCLUDE	OUTDECM.ASM

	END	MAIN