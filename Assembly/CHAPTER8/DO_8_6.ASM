.MODEL	SMALL
.STACK	100H
.DATA
	VarA	DW	?
	VarB	DW	?
.CODE
MAIN	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,0DH
	INT	21H

	CALL	INDECM
	MOV	VarA,AX
	CALL	INDECM
	MOV	VarB,AX

	CMP	AX,0
	JGE	@NEXT
	NEG	AX	;|B|
@NEXT:
	IMUL	VarB	;|B|*B
	MOV	BX,AX

	MOV	AX,VarA
	IMUL	VarB	;A*B
	SUB	BX,AX	;|B|*B - A*B

	MOV	AX,5
	IMUL	VarA	;5*A
	ADD	BX,AX	;|B|*B - A*B + 5*A

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	AX,BX
	CALL	OUTDECM
	MOV	AH,4CH
	INT	21H
MAIN	ENDP

INCLUDE	INDECM.ASM
INCLUDE	OUTDECM.ASM

	END	MAIN