.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	XOR	BX,BX
	MOV	CL,4	;4바이트씩 이동
	MOV	AH,1
	INT	21H
@WHILE:
	CMP	AL,0DH
	JE	@END_WHILE
	CMP	AL,39H
	JG	@LETTER

	AND	AL,0FH	;숫자로 변환

	JMP	@SHIFT
@LETTER:
	SUB	AL,37H	;문자 -> 숫자로 변환
@SHIFT:
	SHL	BX,CL	;4바이트 왼쪽 이동
	OR	BL,AL	;겹치기

	INT	21H	;다음 입력
	JMP	@WHILE

@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	AX,BX		;값을 AX로 이동
	CALL	OUTDECM	;10진수로 출력한다

	MOV	AH,4CH
	INT	21H
MAIN	ENDP

INCLUDE	OUTDECM.ASM

	END	MAIN