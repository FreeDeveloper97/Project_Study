.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	XOR	CX,CX
	MOV	AH,1
	INT	21H
@WHILE:
	CMP	AL,0DH
	JE	@END_WHILE
	CMP	AL,'a'
	JB	@NEXT
	CMP	AL,'z'
	JA	@NEXT

	PUSH	AX
	INC	CX
@NEXT:
	INT	21H
	JMP	@WHILE

@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	JCXZ	@EXIT

	MOV	AH,2
@OUTPUT:
	POP	DX
	AND	DL,0DFH		;대문자 변환
	INT	21H
	LOOP	@OUTPUT
@EXIT:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN