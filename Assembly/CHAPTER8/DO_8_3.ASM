.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	CALL	INDECM

	MOV	CX,AX

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CALL	FACTORIAL

	CALL	OUTDECM

	MOV	AH,4CH
	INT	21H
MAIN	ENDP

FACTORIAL	PROC
	MOV	AX,1
@TOP:
	MUL	CX	;CX가 감소되면서 계속 곱해진다
	LOOP	@TOP
	RET
FACTORIAL	ENDP

INCLUDE	INDECM.ASM
INCLUDE	OUTDECM.ASM

	END	MAIN