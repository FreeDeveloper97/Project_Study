.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	CALL	INDECM
	PUSH	AX

	CALL	INDECM	;B 값을 AX에 넣는다
	
	POP	BX	;A 값을 BX에 불러온다
	IMUL	BX	;A*B -> AX에 저장

	CMP	BX,0
	JGE	@NEXT
	NEG	BX	;A의 절댓값 변환

@NEXT:
	SUB	AX,BX	;A*B - |A|
	PUSH	AX

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	POP	AX
	CALL	OUTDECM
	MOV	AH,4CH
	INT	21H
MAIN	ENDP

INCLUDE	INDECM.ASM
INCLUDE	OUTDECM.ASM

	END	MAIN