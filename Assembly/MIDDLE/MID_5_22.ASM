	CMP	DH,'A'
	JB	@END
	CMP	DH,'Z'
	JA	@END
	INC	SP
	MOV	DL,DH
	INT	21H
@END:
	CMP	SP,0
	JNE	@EEND
	MOV	AH,9
	LEA	DX,ERROR
	INT	21H
@EEND:
	MOV	AH,4CH
	INT	21H
