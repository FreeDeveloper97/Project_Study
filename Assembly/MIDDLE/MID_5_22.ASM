	ERROR	DB	'NONE$'
.CODE
MAIN	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H
	MOV	BH,AL

	INT	21H
	MOV	BL,AL

	INT	21H
	MOV	CH,AL

	INT	21H
	MOV	CL,AL

	INT	21H
	MOV	DH,AL

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	SP,0

	CMP	BH,'A'
	JB	@NEXT
	CMP	BH,'Z'
	JA	@NEXT
	INC	SP
	MOV	DL,BH
	INT	21H
