.MODEL	SMALL
.STACK	100H
.DATA
	ERR_MSG		DB	'NO LOWER CASE LETTERS$'
	SPACE_MSG	DB	' $'
.CODE
MAIN	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	CH,0
	MOV	BH,'a'
	MOV	BL,'z'
	MOV	AH,1
@WHILE:
	INT	21H
	CMP	AL,0DH
	JE	@END_WHILE
	CMP	AL,'a'
	JB	@NEXT
	CMP	AL,'z'
	JA	@NEXT
	
	CMP	CH,0
	JE	@FIRST_IN

	CMP	AL,BH
	JA	@MAX_SET
	CMP	AL,BL
	JB	@MIN_SET
	JMP	@NEXT
@MAX_SET:
	MOV	BH,AL
	JMP	@NEXT
@MIN_SET:
	MOV	BL,AL
	JMP	@NEXT
@FIRST_IN:
	MOV	BH,AL
	MOV	BL,AL
	MOV	CH,1
@NEXT:
	JMP	@WHILE

@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	CH,1
	JE	@PRT

	MOV	AH,9
	LEA	DX,ERR_MSG
	INT	21H
	JMP	@END
@PRT:
	MOV	DL,BL
	INT	21H
	MOV	AH,9
	LEA	DX,SPACE_MSG
	INT	21H
	MOV	AH,2
	MOV	DL,BH
	INT	21H
@END:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN