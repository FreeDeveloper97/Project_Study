.MODEL	SMALL
.STACK	100H
.DATA
	ERROR	DB	'Error$'
.CODE
MAIN	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H
	MOV	BH,AL
	INT	21H
	MOV	BL,AL
	INT	21H
	MOV	CH,AL

	CMP	BH,BL
	JLE	@NEXT
	XCHG	BH,BL
@NEXT:
	CMP	BH,CH
	JLE	@NEXT2
	XCHG	BH,CH
@NEXT2:
	CMP	BL,CH
	JLE	@INPUT
	XCHG	BL,CH		;BH < BL < CH
@INPUT:
	INT	21H
	MOV	CL,AL
	SUB	CL,30H

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	CL,1
	JE	@CASE1
	CMP	CL,2
	JE	@CASE2
	CMP	CL,3
	JE	@CASE3
	MOV	AH,9
	LEA	DX,ERROR
	INT	21H
	JMP	@END
@CASE1:
	MOV	DL,BH
	JMP	@PRT
@CASE2:
	MOV	DL,BL
	JMP	@PRT
@CASE3:
	MOV	DL,CH
@PRT:
	INT	21H
@END:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN