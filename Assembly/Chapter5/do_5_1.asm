.MODEL	SMALL
.STACK	100H
.DATA
	ERR	DB	'Error$'
.CODE
MAIN	PROC

	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H
	
	MOV	AH,1
	INT	21H
	MOV	BL,AL

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	BL,'A'
	JB	@ERROR
	CMP	BL,'Z'
	JBE	@UPPER
	CMP	BL,'a'
	JB	@ERROR
	CMP	BL,'z'
	JBE	@LOWER
	JMP	@ERROR
@ERROR:
	MOV	AH,9
	LEA	DX,ERR
	INT	21H
	JMP	@END
@UPPER:
	ADD	BL,20H
	JMP	@PRT
@LOWER:
	SUB	BL,20H
	JMP	@PRT
@PRT:
	MOV	AH,2
	MOV	DL,BL
	INT	21H
@END:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN