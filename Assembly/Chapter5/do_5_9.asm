.MODEL	SMALL
.STACK	100H
.DATA
	CASE1	DB	'SPRING$'
	CASE2	DB	'SUMMER$'
	CASE3	DB	'FALL$'
	CASE4	DB	'WINTER$'
	ERR	DB	'ERROR$'
.CODE
MAIN	PROC

	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H
	MOV	BL,AL
	SUB	BL,30H

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	BL,1
	JE	@JMP1
	CMP	BL,2
	JE	@JMP2
	CMP	BL,3
	JE	@JMP3
	CMP	BL,4
	JE	@JMP4

	LEA	DX,ERR
	JMP	@PRT

@JMP1:
	LEA	DX,CASE1
	JMP	@PRT
@JMP2:
	LEA	DX,CASE2
	JMP	@PRT
@JMP3:
	LEA	DX,CASE3
	JMP	@PRT
@JMP4:
	LEA	DX,CASE4
@PRT:
	MOV	AH,9
	INT	21H

	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN