.MODEL	SMALL
.STACK	100H
.DATA
	ERR	DB	'Error$'
.CODE
MAIN	PROC

	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H
	MOV	BH,AL

	INT	21H
	MOV	BL,AL

	INT	21H
	MOV	CH,AL

	INT	21H
	MOV	CL,AL

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	AH,2

	CMP	CL,'1'
	JE	@CASE1
	CMP	CL,'2'
	JE	@CASE2
	CMP	CL,'3'
	JE	@CASE3
	JMP	@ERROR

@CASE1:
	MOV	DL,BH
	INT	21H
	JMP	@END
@CASE2:
	MOV	DL,BL
	INT	21H
	JMP	@END
@CASE3:
	MOV	DL,CH
	INT	21H
	JMP	@END
@ERROR:
	MOV	AH,9
	LEA	DX,ERR
	INT	21H
@END:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END 	MAIN
