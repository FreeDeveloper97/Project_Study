.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H
	MOV	BL,AL
	INT	21H
	MOV	BH,AL
	INT	21H
	MOV	CL,AL

	CMP	BL,BH
	JLE	@NEXT1
	XCHG	BL,BH

@NEXT1:
	CMP	BL,CL
	JLE	@NEXT2
	XCHG	BL,CL

@NEXT2:
	CMP	BH,CL
	JLE	@OUTPUT
	XCHG	BH,CL

@OUTPUT:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	ADD	BH,20H
	MOV	DL,BH
	INT	21H

	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN