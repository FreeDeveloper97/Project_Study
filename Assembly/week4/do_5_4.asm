.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC

@BEGIN:
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H

	MOV	BL,AL

	CMP	AL,'a'
	JL	@WRONG

	CMP	AL,'z'
	JG	@WRONG

	INT	21H

	MOV	CL,AL
	
	CMP	AL,'a'
	JL	@WRONG

	CMP	AL,'z'
	JG	@WRONG

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	BL,CL
	JLE	@OUTPUT
	XCHG	BL,CL

@OUTPUT:
	MOV	DL,BL
	INT	21H
	MOV	DL,CL
	INT	21H
	JMP	@EXIT

@WRONG:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H
	JMP	@BEGIN

@EXIT:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN