MAKESEED	PROC
	PUSH	DS
	MOV	AX,0
	MOV	DS,AX
	MOV	SI,46CH
	MOV	AX,[SI]
	POP	DS
	MOV	BX,AX
	RET
MAKESEED	ENDP

RAND	PROC
	PUSH	CX
	MOV	AX,BX
@R_LP:
	MUL	BX
	MOV	CL,4
	SHR	AX,CL
	MOV	BX,AX
	MOV	DX,BX
	MOV	CL,4
	SHR	DX,CL
	ADD	BX,DX
	AND	AX,000FH
	CMP	AL,8
	JA	@R_LP
	POP	CX
	INC	AL
	MOV	RAND_[SI],AL
	RET
RAND	ENDP