.MODEL	SMALL
.STACK	100H
.DATA
	ERR_MSG		DB	'NO LOWER CASE LETTERS$'
.CODE
MAIN	PROC
;엔터 입력시 종료
;문자 입력이 있었는지 확인하는 패러티 필요
;소문자 범위가 맞는지 확인 필요
;MIN, MAX 저장하여 작은경우 저장, 큰경우 저장

	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AX,2
	MOV	DL,'?'
	INT	21H

	MOV	CH,0	;CHEAK
	MOV	BH,'a'	;MAX
	DEC	AH
	MOV	BL,'z'	;MIN
	INC	BL

	MOV	AH,1
@WHILE:
	INT	21H
	CMP	AL,0DH
	JE	@END_WHILE
	CMP	AL,'a'
	JB	@NEXT
	CMP	AL,'z'
	JA	@NEXT

	CMP	AL,BH
	JA	@MAX
	CMP	AL,BL
	JB	@MIN
	JMP	@NEXT
@MAX:
	MOV	BH,AL
	CMP	CH,1
	JE	@NEXT
	MOV	CH,1
	JMP	@NEXT
@MIN:
	MOV	BL,AL
	CMP	CH,1
	JE	@NEXT
	MOV	CH,1
@NEXT:
	JMP	@WHILE
@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	CMP	CH,1
	JE	@PRINT
	MOV	AH,9
	LEA	DX,ERR_MSG
	INT	21H
	JMP	@EXIT
@PRINT:
	MOV	DL,BL
	INT	21H
	MOV	DL,BH
	INT	21H
@EXIT:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN
	