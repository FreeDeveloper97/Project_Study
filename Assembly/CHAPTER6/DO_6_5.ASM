.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	BL,0	;카운터 초기화
	MOV	AH,1
	INT	21H
@WHILE:
	CMP	AL,0DH	;엔터를 입력했는지 판별
	JE	@END_WHILE
	SUB	AL,30H
	JE	@NEXT	;JE를 사용하여 0일경우 패스
	INC	BL	;1UP
@NEXT:
	INT	21H
	JMP	@WHILE
@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	ADD	BL,30H
	MOV	DL,BL
	INT	21H
	
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN