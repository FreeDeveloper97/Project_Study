.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
;1 + 2 + ... + N : PRINT HEX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H
	
	MOV	CX,0
	MOV	AH,1
	INT	21H
	SUB	AL,30H

	MOV	CL,AL	;COUNTER
	MOV	BL,0	;SUM
@TOP:
	ADD	BL,CL
	LOOP	@TOP	;SUM

	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	DH,2	;COUNTER : 2
	MOV	CL,4
@OUTPUT:
	MOV	DL,BL	;BL -> DL (12)
	SHR	DL,CL	;(01)

	CMP	DL,0AH
	JL	@NUMBER

	ADD	DL,37H	;A~F : TRANSLATE
	JMP	@NEXT
@NUMBER:
	OR	DL,30H	;0~9 : TRANSLATE
@NEXT:
	INT	21H	;PRINT

	ROL	BL,CL
	DEC	DH
	JNZ	@OUTPUT

	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN
	