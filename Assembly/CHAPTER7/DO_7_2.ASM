.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC

@BEGIN:
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	XOR	BL,BL
	MOV	AH,1
	INT	21H
@WHILE:
	CMP	AL,0DH
	JE	@END_WHILE	;END WHILE

	CMP	AL,'0'
	JE	@BINARY
	CMP	AL,'1'
	JE	@BINARY

	JMP	@ERROR
@BINARY:
	AND	AL,0FH		;TRANSLATE
	SHL	BL,1		;GO LEFT
	OR	BL,AL		;INPUT

	INT	21H
	JMP	@WHILE
@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	CX,8		;PRINT COUNTER : 8

@OUTPUT:
	ROR	BL,1
	JC	@HIGH		;CF=1 : HIGH

	MOV	DL,30H		;CF=0 : 30H
	JMP	@NEXT
@HIGH:
	MOV	DL,31H
@NEXT:
	INT	21H		;PRINT
	LOOP	@OUTPUT	;AGAIN

	JMP	@EXIT
@ERROR:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H
	JMP	@BEGIN
@EXIT:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN