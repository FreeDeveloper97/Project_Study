.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC

@BEGIN:
	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	XOR	BX,BX
	MOV	AH,1
	INT	21H
@WHILE:
	CMP	AL,0DH
	JE	@END_WHILE

	CMP	AL,'0'
	JE	@BINARY
	CMP	AL,'1'
	JE	@BINARY

	JMP	@ERROR
@BINARY:
	AND	AL,0FH
	SHL	BX,1
	OR	BL,AL
	INT	21H

	JMP	@WHILE
@END_WHILE:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H

	MOV	CX,16
	MOV	DH,0	;DH : CHEAK
@OUTPUT:
	ROL	BX,1
	JC	@HIGH

	CMP	DH,0	;CHEAK (1 : 1PRINT, 0 : NOT PRINT)
	JE	@NEXT2	;NOT PRINT

	MOV	DL,30H
	JMP	@NEXT1
@HIGH:
	MOV	DL,31H
	CMP	DH,1
	JE	@NEXT1
	MOV	DH,1	;DH 0 -> 1 CHANGE
@NEXT1:
	INT	21H	;PRINT
@NEXT2:
	LOOP	@OUTPUT	;REPEAT
	JMP	@EXIT

@ERROR:
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H
	JMP	@BEGIN
@EXIT:
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN