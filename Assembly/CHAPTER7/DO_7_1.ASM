.MODEL	SMALL
.STACK	100H
.DATA
	ODD_PRT		DB	0DH,0AH,'ODD$'
	EVEN_PRT	DB	0DH,0AH,'EVEN$'
.CODE
	MAIN	PROC
	
	MOV	AX,@DATA
	MOV	DS,AX

	MOV	AH,2
	MOV	DL,'?'
	INT	21H

	MOV	AH,1
	INT	21H	;1ST IN

	TEST	AL,01H
	JE	@EVEN
	LEA	DX,ODD_PRT
	JMP	@OUTPUT
@EVEN:
	LEA	DX,EVEN_PRT
@OUTPUT:
	MOV	AH,9
	INT	21H

	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END	MAIN